let day = 2;


switch(day){
    
    case 1:
        console.log(`it's monday`);
        break;
        case 2:
        console.log(`it's tuesday`);
        break;
        case 3:
        console.log(`it's wednesday`);
        break;
        case 4:
        console.log(`it's thursday`);
        break;
        case 5:
        console.log(`it's friday`);
        break;
        case 6:
        console.log(`it's saturday`);
        break;
        case 7:
        console.log(`it's sunday`);
        break;
        default:
            console.log(`${day} is not a day`)

                


}